#define FIND_BUCKET_LOOKAHEAD 4

static uint64_t *find_bucket_end( uint64_t *bucket_begin, uint64_t *data_end, uint64_t in_bucket_max )
{
  size_t max = data_end - bucket_begin;
  size_t i = FIND_BUCKET_LOOKAHEAD;

  while( i < max && bucket_begin[ i ] <= in_bucket_max )
    i += FIND_BUCKET_LOOKAHEAD;

  for( i -= FIND_BUCKET_LOOKAHEAD; i < max; ++i )
    if( bucket_begin[ i ] > in_bucket_max )
      return bucket_begin + i;

  return bucket_begin + max;
}

#define BUILD_HISTOGRAM_LOOKAHEAD 64 // Must be >= 8.

static uint64_t *find_bucket_end_while_building_histogram(
  uint64_t *bucket_begin,
  uint64_t *data_end,
  int shift,
  uint64_t in_bucket_max,
  size_t *child_bucket_counts // Out.
)
{
  uint64_t *bucket_end = bucket_begin;
  uint64_t *chunk_end;
  while(
    data_end - bucket_end > BUILD_HISTOGRAM_LOOKAHEAD &&
    *( chunk_end = bucket_end + BUILD_HISTOGRAM_LOOKAHEAD ) <= in_bucket_max
  )
  {
    int i = 0;
    do
    {
      uint64_t x0 = bucket_end[ 0 ];
      uint64_t x1 = bucket_end[ 1 ];
      uint64_t x2 = bucket_end[ 2 ];
      uint64_t x3 = bucket_end[ 3 ];
      uint64_t x4 = bucket_end[ 4 ];
      uint64_t x5 = bucket_end[ 5 ];
      uint64_t x6 = bucket_end[ 6 ];
      uint64_t x7 = bucket_end[ 7 ];
      ++child_bucket_counts[ x0 >> shift & 0xFF ];
      ++child_bucket_counts[ x1 >> shift & 0xFF ];
      ++child_bucket_counts[ x2 >> shift & 0xFF ];
      ++child_bucket_counts[ x3 >> shift & 0xFF ];
      ++child_bucket_counts[ x4 >> shift & 0xFF ];
      ++child_bucket_counts[ x5 >> shift & 0xFF ];
      ++child_bucket_counts[ x6 >> shift & 0xFF ];
      ++child_bucket_counts[ x7 >> shift & 0xFF ];
      i += 8;
      bucket_end += 8;
    } while( i < BUILD_HISTOGRAM_LOOKAHEAD );
  }

  while( bucket_end != data_end && *bucket_end <= in_bucket_max )
  {
    ++child_bucket_counts[ ( *bucket_end >> shift ) & 0xFFull ];
    ++bucket_end;
  }

  return bucket_end;
}

#define SMALL_BUCKET_THRESHOLD 64

void polite_sort( uint64_t *data_begin, uint64_t *data_end )
{
  if( data_end - data_begin <= SMALL_BUCKET_THRESHOLD )
  {
    sort_small_bucket( data_begin, data_end );
    return;
  }

  uint64_t *bucket_begin = data_begin;
  int shift = 56;
  while( true )
  {
    uint64_t in_bucket_msd_sequence_mask = ~(uint64_t)0xFF << shift;
    uint64_t in_bucket_max = *bucket_begin | ~in_bucket_msd_sequence_mask;

    uint64_t *bucket_end;

    if(
      data_end - bucket_begin > SMALL_BUCKET_THRESHOLD &&
      *( bucket_begin + SMALL_BUCKET_THRESHOLD ) <= in_bucket_max
    )
    {
      // The current bucket has more elements than the small-bucket threshold, so do a radix sort step.
      // Because the child buckets' element counts are not used after their bounds are determined, we store the child
      // bucket's counts and ends in the same block of memory.

      alignas( 64 ) uint64_t *child_bucket_begins[ 256 ];
      union
      {
        alignas( 64 ) size_t child_bucket_counts[ 256 ];
        alignas( 64 ) uint64_t *child_bucket_ends[ 256 ];
      } u = { 0 };

      // Determine child buckets' element count and, simultaniously, the current bucket's end.

      bucket_end = find_bucket_end_while_building_histogram(
        bucket_begin,
        data_end,
        shift,
        in_bucket_max,
        u.child_bucket_counts
      );

      // Use the counts to determine the child buckets' bounds.

      int nonempty_bucket_count = 0;
      uint64_t *child_bucket_begin = bucket_begin;
      for( int i = 0; i < 256; ++i )
      {
        nonempty_bucket_count += (bool)u.child_bucket_counts[ i ];
        child_bucket_begins[ i ] = child_bucket_begin;
        child_bucket_begin += u.child_bucket_counts[ i ];
        u.child_bucket_ends[ i ] = child_bucket_begin;
      }

      // Permute.
      // Skipping this stage if there is only bucket achieves a significant performance boost, especially when the range
      // of the values being sorted is significantly smaller than the range of the datatype.

      if( nonempty_bucket_count > 1 )
        for( int i = 0; i < 256; i++ )
        {
          while( child_bucket_begins[ i ] != u.child_bucket_ends[ i ] )
          {
            int destination_child_bucket;
            if( ( destination_child_bucket = *child_bucket_begins[ i ] >> shift & 0xFF ) != i )
            {
              uint64_t temp = *child_bucket_begins[ i ];
              uint64_t swap;
              do
              {
                swap = temp;
                temp = *child_bucket_begins[ destination_child_bucket ];
                *( child_bucket_begins[ destination_child_bucket ]++ ) = swap;

                // Fetch the next destination-bucket so that it's ready for use next time we need it.
                // This optimization proved to be significant in benchmarking.
                __builtin_prefetch( child_bucket_begins[ destination_child_bucket ] );

                destination_child_bucket = temp >> shift & 0xFF;
              } while( destination_child_bucket != i );

              *( child_bucket_begins[ i ]++ ) = temp;
            }
            else
              ++child_bucket_begins[ i ];
          }
        }

      // Use the small-bucket sort for child buckets with fewer elements than the radix sort threshold, appying two
      // optimizations:
      // * Move the bucket_begin pointer to the end of any contiguous sequence of sorted child buckets at the start of
      //   the current bucket so that we don't unnecessarily scan them again after we moving down to the next level.
      // * If all child buckets are sorted at this stage, we can avoid moving down at all.

      child_bucket_begin = bucket_begin;
      for( int i = 0; i < 256; ++i )
      {
        size_t bucket_count = u.child_bucket_ends[ i ] - child_bucket_begin;
        if( bucket_count <= SMALL_BUCKET_THRESHOLD )
        {
          if( bucket_count > 1 )
            sort_small_bucket( child_bucket_begin, u.child_bucket_ends[ i ] );

          if( child_bucket_begin == bucket_begin )
            bucket_begin = u.child_bucket_ends[ i ];
        }

        child_bucket_begin = u.child_bucket_ends[ i ];
      }

      // Move down one level, if possible.
      if( shift != 0 && bucket_begin != bucket_end )
      {
        shift -= 8;
        continue;
      }
    }
    else
      // The current bucket has fewer elements than the small-bucket threshold.
      // As it was necessarily already sorted as the previous level, just find the end.
      bucket_end = find_bucket_end( bucket_begin, data_end, in_bucket_max );

    if( bucket_end == data_end ) // All done.
      return;

    // The next level to jump up or across to is the position of the highest differing byte, minus one.
    bucket_begin = bucket_end;
    shift = 48 - ( __builtin_clzll( *bucket_end ^ in_bucket_max ) & ~7 );
  }
}

